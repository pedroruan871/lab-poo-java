package lab04;

public class Repeticoes1 {
    // java lab04/Repeticoes1.java
    public static void main(String[] args) {
        /**
         * while: Executa um bloco de código enquanto uma condição específica for
         * verdadeira.
         */
        var numero = 0;
        while (numero < 10) {
            System.out.println(numero);
            numero += 1;
        }
    }
}
